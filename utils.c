int	is_nuber(char *str)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (i < strlen(str))
	{
		if ('0' <= str[i] && str[i] <= '9')
			j++;
		i++;		
	}
	return (i == j);
}
